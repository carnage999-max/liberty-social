interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
  onClose: () => void;
}

export function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const pickerRef = useRef<HTMLDivElement>(null);
  const [activeCategory, setActiveCategory] = useState<string>(
    Object.keys(EMOJI_CATEGORIES)[0]
  );

  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (pickerRef.current && !pickerRef.current.contains(event.target as Node)) {
        onClose();
      }
    };

    const handleEscape = (event: KeyboardEvent) => {
      if (event.key === "Escape") {
        onClose();
      }
    };

    document.addEventListener("mousedown", handleClickOutside);
    document.addEventListener("keydown", handleEscape);

    return () => {
      document.removeEventListener("mousedown", handleClickOutside);
      document.removeEventListener("keydown", handleEscape);
    };
  }, [onClose]);

  const handleEmojiClick = (emoji: string) => {
    onSelect(emoji);
  };

  return (
    <div
      ref={pickerRef}
      className="absolute bottom-full left-0 z-50 mb-2 w-80 rounded-lg border border-gray-200 bg-white shadow-2xl"
      role="menu"
      aria-label="Emoji picker"
      onClick={(e) => e.stopPropagation()}
      onMouseDown={(e) => e.stopPropagation()}
    >
      {/* Category tabs */}
      <div className="flex border-b border-gray-200 overflow-x-auto">
        {Object.keys(EMOJI_CATEGORIES).map((category) => (
          <button
            key={category}
            type="button"
            onClick={() => setActiveCategory(category)}
            className={`
              px-3 py-2 text-sm font-medium whitespace-nowrap transition
              ${activeCategory === category
                ? "text-blue-600 border-b-2 border-blue-600"
                : "text-gray-600 hover:text-gray-900"
              }
            `}
          >
            {category.split(" ")[0]}
          </button>
        ))}
      </div>

      {/* Emoji grid */}
      <div className="h-64 overflow-y-auto p-3">
        <div className="grid grid-cols-8 gap-1">
          {EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES].map(
            (emoji, index) => (
              <button
                key={`${activeCategory}-${index}`}
                type="button"
                onClick={() => handleEmojiClick(emoji)}
                className="
                  flex h-9 w-9 items-center justify-center rounded-lg
                  text-xl transition hover:bg-gray-100
                  focus:outline-none focus:ring-2 focus:ring-blue-500
                "
                aria-label={`Emoji: ${emoji}`}
              >
                {emoji}
              </button>
            )
          )}
        </div>
      </div>
    </div>
  );
}

